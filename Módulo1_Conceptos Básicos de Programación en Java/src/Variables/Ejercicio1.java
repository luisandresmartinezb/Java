package Variables;

public class Ejercicio1 {

	public static void main(String[] args) 
	{
		// Variable para almacenar el nombre de una persona
				String nombrePersona ="Luis Andres";
				// Variable “x” para almacenar un número con 2 decimales
		        float x = 45.78f;

		        // Variable constante para almacenar el valor del dólar
		        final double VALOR_DOLAR = 1.23;

		        // Variable primitiva float para almacenar los primeros 7 decimales de “pi”
		        float pi = 3.1415927f;

		        // Variable de estructura tipo Double para almacenar los primeros 15 decimales de “Euler”
		        double euler = 2.718281828459045;
		        
		        
		     // Imprimir las variables en consola
		        System.out.println("Nombre de la persona: " + nombrePersona);
		        System.out.println("Valor de x: " + x);
		        System.out.println("Valor del dólar: " + VALOR_DOLAR);
		        System.out.println("Valor de pi: " + pi);
		        System.out.println("Valor de Euler: " + euler);
		        
	}

}
